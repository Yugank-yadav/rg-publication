// Helper functions for product data
export const getProductById = (id) => {
  return products.find((product) => product.id === parseInt(id));
};

export const getRelatedProducts = (productId, limit = 4) => {
  const product = getProductById(productId);
  if (!product) return [];

  return products
    .filter(
      (p) =>
        p.id !== product.id &&
        (p.subject === product.subject || p.class === product.class)
    )
    .slice(0, limit);
};

export const getProductsByCategory = (category) => {
  return products.filter((product) => product.category === category);
};

export const getProductsBySubject = (subject) => {
  return products.filter((product) => product.subject === subject);
};

export const getProductsByClass = (classLevel) => {
  return products.filter((product) => product.class === classLevel);
};

